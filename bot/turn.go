package bot

import "sort"

type botTurn struct {
	maxTick int
	ticks   []int
	fnTurn  func()
}

func NewBotTurn(minTick, maxTick int, maxOccur int, fnTurn func()) *botTurn {
	b := &botTurn{
		maxTick: maxTick,
		fnTurn:  fnTurn,
	}
	if maxOccur <= 0 {
		maxOccur = 1
	}
	if minTick >= maxTick {
		minTick = maxTick - 1
	}
	numOccur := RandomInt(1, maxOccur+1)

	for i := 0; i < numOccur*3; i++ {
		tick := RandomInt(minTick, maxTick)
		for _, v := range b.ticks {
			if v == tick {
				continue
			}
		}
		b.ticks = append(b.ticks, tick)
		if len(b.ticks) == numOccur {
			break
		}
	}
	sort.Slice(b.ticks, func(i, j int) bool {
		a := b.ticks[i]
		b := b.ticks[j]
		return a < b
	})
	return b
}

func (b *botTurn) Loop() bool {
	if len(b.ticks) == 0 {
		return false
	}
	isAllFinish := false
	for idx, tick := range b.ticks {
		if tick < 0 {
			continue
		}
		isAllFinish = true
		if tick >= 0 {
			tick--
		}
		b.ticks[idx] = tick
		if tick == 0 {
			if b != nil {
				b.fnTurn()
			}
		}
	}
	return isAllFinish
}
